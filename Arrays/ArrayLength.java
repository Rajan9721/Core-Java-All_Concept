package Arrays;

public class ArrayLength {
    public static void main(String args[]){

// Length of the array
        int a[] = {5, 6, 8, 9};
        System.out.println("Length: "+ a.length);

        // Print array
        for(int i=0; i<a.length; i++){
            System.out.println("Array elements: "+ a[i]);
        }

    }
}